print(int(3.8)) #int는 3.8의 소수부분인 .8을 빼고 출력한다
print(float(3)) #float는 괄호 안의 값을 소수형으로 출력한다
print(int("2")+int("5")) #문자열2와 5가 정수형 2와 5로 변환
print(float("1.1")+float("2.5")) #문자열 1.1과 2.5가 소수형 1.1과 2.5로 변환
print(str(2)+str(5)) #정수형 2와 5를 문자열 2와 5로 변환시켜 출력
